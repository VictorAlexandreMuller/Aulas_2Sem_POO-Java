    private void textFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textFiltroKeyReleased
        // TODO add your handling code here:
          ArrayList<Funcionario> listaFiltrada =
      listaFuncionarios.stream()
           .filter(f -> 
            f.getNome().startsWith(textFiltro.getText()))
           .collect(Collectors.toCollection(ArrayList::new));
        
        TableModel tb = TableModelCreator.createTableModel(
                Funcionario.class, listaFiltrada,null);
        jTable1.setModel(tb);
    }//GEN-LAST:event_textFiltroKeyReleased

